    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            String[] datos = new String[2];
            datos = mt.edad();
            lblEdad.setText("El jugador mas joven es " + datos[0] + " con " + datos[1] + " a√±os");
            datos = mt.peso();
            lblPeso.setText("El jugador mas pesado es " + datos[0] + " con " + datos[1] + " libras");
            datos = mt.altura();
            lblAltura.setText("El jugador mas alto es " + datos[0] + " con " + datos[1] + " metros");
            mt.limpiar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al procesar los datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    /**
     * metodo para refrescar el jlist
     */
    void refrescar() {
        modelo.clear();
        ArrayList<String> consulta = new ArrayList<>();
        consulta = mt.consulta();
        for (int i = 0; i < consulta.size(); i++) {
            modelo.addElement(consulta.get(i));

        }
        lstJugadores.setModel(modelo);
    }

