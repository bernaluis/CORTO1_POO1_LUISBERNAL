/**
     *
     * @param evt Boton de ingreso de nuevo jugador
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if (txtEdad.getText().length() > 0 && txtPeso.getText().length() > 0 && txtNombre.getText().length() > 0 && txtAltura.getText().length() > 0) {
                int edad = Integer.parseInt(txtEdad.getText());
                double peso = Double.parseDouble(txtPeso.getText());
                double altura = Double.parseDouble(txtAltura.getText());
                mt.agregar(txtNombre.getText(), edad, peso, altura);
                JOptionPane.showMessageDialog(null, "Jugador ingresado correctamente", "Exito", JOptionPane.INFORMATION_MESSAGE);
                txtNombre.setText("");
                txtEdad.setText("");
                txtAltura.setText("");
                txtPeso.setText("");
                txtNombre.setFocusable(true);
                refrescar();
            } else {
                JOptionPane.showMessageDialog(null, "Jugador no ingresado, debe completar todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Jugador no ingresado, tipo de dato incorrecto " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
